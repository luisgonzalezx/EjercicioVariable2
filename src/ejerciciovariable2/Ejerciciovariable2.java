package ejerciciovariable2;


public class Ejerciciovariable2 {

    
    public static void main(String[] args) {
        
        int x=5;
        int y=100;
        double n=40.2;
        double m=1.25;
        
        System.out.println("la suma de las variables es: "+(x+y));
        System.out.println("la diferencia de las variables es: "+(x-y));
        System.out.println("el producto de las variables es: "+(x*y));
        System.out.println("el resto de las variables es: "+(x%y));
        System.out.println("la suma de las variables es: "+(n+m));
        System.out.println("la diferencia de las variables es: "+(n-m));
        System.out.println("el producto de las variables es: "+(n*m));
        System.out.println("el resto de las variables es: "+(n/m));
        System.out.println("el resto de las variables es: "+(x/n));
        System.out.println("el resto de las variables es: "+(y/m));
        System.out.println("el resto de las variables es: "+(y%n));
        System.out.println("el resto de las variables es: "+(x+y+n+m));
        System.out.println("el resto de las variables es: "+(x*y*n*m));       
    }
    
}
